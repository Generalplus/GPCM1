/*---------------------------------------------------------------------------------------
 * Header File Included Area
 *---------------------------------------------------------------------------------------*/
#include "GPCM1Fx.h"
#include "system_GPCM1Fx.h"


/*---------------------------------------------------------------------------------------
 * Gobal Variable Declaration Area
 *---------------------------------------------------------------------------------------*/
uint32_t SystemCoreClock = 8192000UL;


/*---------------------------------------------------------------------------------------
 * Subroutine Area
 *---------------------------------------------------------------------------------------*/  

/** 
 * @brief  
 *         
 * @param 
 *   None.
 * @return 
 *   None.
 */
void SystemInit(void)
{	
	PLL_Enable();
	PLL_ChangeClk(PLL_81D920M);	
	HCLK_SrcSel(HCLK_SEL_PLL);
}

/** 
 * @brief  
 *         
 * @param 
 *   None.
 * @return 
 *   None.
 */
void SystemCoreClockUpdate(void)
{
  SystemCoreClock = System_GetCoreClock();	
}
